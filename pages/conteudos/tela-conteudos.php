    <script>
      $(document).ready(function() {
        const url_atual = window.location.href;
        let page = url_atual.split("/");
        let titulo = page[page.length - 1].split("-");
        let local = "";

        for (let index = 0; index < titulo.length; index++) {
          local += `${titulo[index]} `;
        }

        $.get("back-end/controller/ControllerListagem.php", {
          link: local
        }, function(retorno) {
          const dados = JSON.parse(retorno);

          $(".local").text(dados.link)
          $(".texto-link").html((dados.texto).replaceAll("\n", "<br>"));
          $(".img-link").attr("src", `admin/img/links/${dados.nome_img}`);
        });
      });
    </script>

